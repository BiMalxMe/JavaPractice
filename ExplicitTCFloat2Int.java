public class ExplicitTCFloat2Int {
    public static void main(String[] args) {
        float floatValue = 342.235f;

      //Its your boy BML casting into int
        int intValue = (int) floatValue;

        // Yap casted Sucessfully
        System.out.println("Original: " + floatValue);
        System.out.println("Casted " + intValue);
    }
}
